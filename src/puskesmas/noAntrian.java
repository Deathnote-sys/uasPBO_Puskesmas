    private void btn_selesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_selesaiActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableantrian.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.");
        return;
    }
    
    // Ambil nilai no_antrian dari baris yang dipilih
    int noAntrian = (int) tableantrian.getValueAt(selectedRow, 0);

    try {
        Connection con = koneksi.getConnection();
        String sql = "DELETE FROM antrian WHERE no_antrian = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, noAntrian);
        
        int deletedRows = pst.executeUpdate();
        if (deletedRows > 0) {
            JOptionPane.showMessageDialog(this, "Data antrian berhasil dihapus.");
            
            // Hapus baris dari model tabel
            DefaultTableModel model = (DefaultTableModel) tableantrian.getModel();
            model.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data antrian.");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data antrian.");
    }
    }//GEN-LAST:event_btn_selesaiActionPerformed

