    private void tampilkan() {
       try {
            Connection con = koneksi.getConnection();
            String sql = "SELECT no_antrian, poli, jenis_layanan, status FROM antrian";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel) tableantrian.getModel();
            model.setRowCount(0); // Bersihkan tabel sebelum menambahkan data baru
            
            while (rs.next()) {
                int noAntrian = rs.getInt("no_antrian");
                String poli = rs.getString("poli");
                String jenisLayanan = rs.getString("jenis_layanan");
                String status = rs.getString("status");
                
                // Tambahkan data ke dalam tabel
                model.addRow(new Object[]{noAntrian, poli, jenisLayanan,status});
            }
           
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
